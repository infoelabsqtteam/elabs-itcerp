    /** save amendment record
    *  Show the form for creating a new resource.
    * created_by : Ruby
	 * created_on : 27-12-2018
    * @return \Illuminate\Http\Response
    */
    public function createAmendment(Request $request){
	
	$returnData = array();
	
	$error    = '0';
	$message  = config('messages.message.error');
	$data     = '';
	
	if($request->isMethod('post') && !empty($request['data']['formData'])){
	
	    //pasrse searlize data 
	    $newPostData = array();
	    
	    parse_str($request['data']['formData'], $newPostData);
	    unset($newPostData['_token']);
	    
		 
	    if(empty($newPostData['oam_name'])){
		$returnData = array('error' => config('messages.message.amendmentNameRequired'));
	    }else if(empty($newPostData['oam_code'])){
		$returnData = array('error' => config('messages.message.amendmentCodeRequired'));
	    }else if(isset($newPostData['oam_status']) && $newPostData['oam_status'] == ''){
		$returnData = array('error' => config('messages.message.amendmentStatusRequired'));
	    }else{		
		try{
		    //Adding New Dynamic Fields
		    $formData['oam_name']   = ucwords(strtolower($newPostData['oam_name']));
		    $formData['oam_code']   = $newPostData['oam_code'];
		    $formData['oam_status'] = $newPostData['oam_status'];
		    //echo'<pre>$formData'; print_r($formData); echo'</pre>';die;
		    if(!empty($formData['oam_name'])){
			$saveAmendment = DB::table('order_amendment_master')->insertGetId($formData);	
			if(!empty($saveAmendment)){
			    $error   = '1';
			    $data    = $saveAmendment;
			    $message = config('messages.message.saved');                                    
			}
		    }
		}catch(\Illuminate\Database\QueryException $ex){
		    $message = config('messages.message.exist');
		}
	    }	
	}else{
	    $message  = config('messages.message.error');
	}
	
	return response()->json(array('error'=> $error,'message'=> $message,'data'=> $data));	
    }

