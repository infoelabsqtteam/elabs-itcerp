    /**
     * Show the form for editing the specified resource.
     * created_by : Ruby
	  * created_on : 27-12-2018
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function editAmendment(Request $request)
    {
		$returnData = array();
		if ($request->isMethod('post')) {
			if(!empty($request['data']['id'])){
				$amendData = DB::table('order_amendment_master')
								->where('order_amendment_master.oam_id', '=', $request['data']['id'])
								->first();
				
				if($amendData->oam_id){
					$returnData = array('responseData' => $amendData);				
				}else{
					$returnData = array('error' => config('messages.message.noRecordFound'));
				}
			}else{
				$returnData = array('error' => config('messages.message.dataNotFoundToSaved'));
			}
		}else{
				$returnData = array('error' => config('messages.message.provideAppData'));			
		}
		return response()->json($returnData);	
    }
    /**
     * Update the specified resource in storage.
     * created_by : Ruby
	  * created_on : 27-12-2018
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function updateAmendment(Request $request){
    
		  $returnData = array();
		  if ($request->isMethod('post')) {
				if(!empty($request['data']['formData'])){   
					//pasrse searlize data 
					 $newPostData = array();
					 parse_str($request['data']['formData'], $newPostData);
					 //echo'<pre>'; print_r($newPostData); die;
					 if(empty($newPostData['oam_name'])){
						  $returnData = array('error' => config('messages.message.amendmentNameRequired'));
					 }else if(empty($newPostData['oam_code'])){
						  $returnData = array('error' => config('messages.message.amendmentCodeRequired'));
					 }else if(isset($newPostData['oam_status']) && $newPostData['oam_status'] == ''){
						  $returnData = array('error' => config('messages.message.amendmentStatusRequired'));
					 }else{
						  try{
							$updated = DB::table('order_amendment_master')->where('oam_id',$newPostData['oam_id'])->update([
								'oam_status' 	=> $newPostData['oam_status'],
								'oam_code' 		=> $newPostData['oam_code'],
								'oam_name' 		=> ucwords($newPostData['oam_name']),
						   ]);
								if(!empty($updated)){
									 $returnData = array('success' => config('messages.message.amendmentUpdated'));  
								}else{
									$returnData = array('success' => config('messages.message.savedNoChange'));   
								}
						  }catch(\Illuminate\Database\QueryException $ex){
								$returnData = array('error' => config('messages.message.exist')); 
						  }
					 }
				}else{
					$returnData = array('error' =>  config('messages.message.dataNotFound'));
				}
		  }else{
			  $returnData = array('error' => config('messages.message.dataNotFound'));
		  } 
		  return response()->json($returnData);
    }

    /**
     * Remove the specified resource from storage.
     * created_by : Ruby
	  * created_on : 27-12-2018
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function deleteAmendment(Request $request)
    {
		$returnData = array();
		if ($request->isMethod('post')){
			if(!empty($request['data']['id'])){
				try { 
					$amendment = DB::table('order_amendment_master')->where('oam_id', $request['data']['id'])->delete();
					if($amendment){
						$returnData = array('success' => config('messages.message.amendmentDeleted'));
					}else{
						$returnData = array('error' => config('messages.message.amendmentNotDeleted'));					
					}
				}catch(\Illuminate\Database\QueryException $ex){ 
				   $returnData = array('error' => config('messages.message.foreignKeyDeleteError'));
				}
			}else{
				$returnData = array('error' => config('messages.message.noRecordFound'));
			}
		}
		return response()->json($returnData);
    }
