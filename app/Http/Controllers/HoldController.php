    /** save amendment record
    *  Show the form for creating a new resource.
    * created_by : Ruby
	 * created_on : 27-12-2018
    * @return \Illuminate\Http\Response
    */
    public function create(Request $request){
	
	$returnData = array();
	
	$error    = '0';
	$message  = config('messages.message.error');
	$data     = '';
	
	if($request->isMethod('post') && !empty($request['data']['formData'])){
	
	    //pasrse searlize data 
	    $newPostData = array();
	    
	    parse_str($request['data']['formData'], $newPostData);
	    unset($newPostData['_token']);
	    
		 
	    if(empty($newPostData['oh_name'])){
		$returnData = array('error' => config('messages.message.holdtNameRequired'));
	    }else if(empty($newPostData['oh_code'])){
		$returnData = array('error' => config('messages.message.holdCodeRequired'));
	    }else if(isset($newPostData['oh_status']) && $newPostData['oh_status'] == ''){
		$returnData = array('error' => config('messages.message.holdStatusRequired'));
	    }else{		
		  try{
				//Adding New Dynamic Fields
				$formData['oh_name']   = ucwords(strtolower($newPostData['oh_name']));
				$formData['oh_code']   = $newPostData['oh_code'];
				$formData['oh_status'] = $newPostData['oh_status'];
				//echo'<pre>$formData'; print_r($formData); echo'</pre>';die;
				if(!empty($formData['oh_name'])){
				$save = DB::table('order_hold_master')->insertGetId($formData);	
					 if(!empty($save)){
						  $error   = '1';
						  $data    = $save;
						  $message = config('messages.message.saved');                                    
					 }
				}
		  }catch(\Illuminate\Database\QueryException $ex){
		    $message = config('messages.message.exist');
		  }
	    }	
	}else{
	    $message  = config('messages.message.error');
	}
	
	return response()->json(array('error'=> $error,'message'=> $message,'data'=> $data));	
    }

