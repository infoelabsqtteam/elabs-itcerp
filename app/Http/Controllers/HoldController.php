    /**
     * Show the form for editing the specified resource.
     * created_by : Ruby
	  * created_on : 27-12-2018
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit(Request $request)
    {
		$returnData = array();
		if ($request->isMethod('post')) {
			if(!empty($request['data']['id'])){
				$amendData = DB::table('order_hold_master')
								->where('order_hold_master.oh_id', '=', $request['data']['id'])
								->first();
				
				if($amendData->oh_id){
					$returnData = array('responseData' => $amendData);				
				}else{
					$returnData = array('error' => config('messages.message.noRecordFound'));
				}
			}else{
				$returnData = array('error' => config('messages.message.dataNotFoundToSaved'));
			}
		}else{
				$returnData = array('error' => config('messages.message.provideAppData'));			
		}
		return response()->json($returnData);	
    }
    /**
     * Update the specified resource in storage.
     * created_by : Ruby
	  * created_on : 27-12-2018
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request){
    
		  $returnData = array();
		  if ($request->isMethod('post')) {
				if(!empty($request['data']['formData'])){   
					//pasrse searlize data 
					 $newPostData = array();
					 parse_str($request['data']['formData'], $newPostData);
					 //echo'<pre>'; print_r($newPostData); die;
					 if(empty($newPostData['oh_name'])){
						  $returnData = array('error' => config('messages.message.holdNameRequired'));
					 }else if(empty($newPostData['oh_code'])){
						  $returnData = array('error' => config('messages.message.holdCodeRequired'));
					 }else if(isset($newPostData['oh_status']) && $newPostData['oh_status'] == ''){
						  $returnData = array('error' => config('messages.message.holdStatusRequired'));
					 }else{
						  try{
						  $updated = DB::table('order_hold_master')->where('order_hold_master.oh_id',$newPostData['oh_id'])->update([
								'oh_status' 	=> $newPostData['oh_status'],
								'oh_code' 		=> $newPostData['oh_code'],
								'oh_name' 		=> ucwords($newPostData['oh_name']),
						  ]);
						
						  if(!empty($updated)){
								 $returnData = array('success' => config('messages.message.holdUpdated'));  
							}else{
								$returnData = array('success' => config('messages.message.savedNoChange'));   
							}
						  }catch(\Illuminate\Database\QueryException $ex){
								$returnData = array('error' => config('messages.message.exist')); 
						  }
					 }
				}else{
					$returnData = array('error' =>  config('messages.message.dataNotFound'));
				}
		  }else{
			  $returnData = array('error' => config('messages.message.dataNotFound'));
		  } 
		  return response()->json($returnData);
    }

    /**
     * Remove the specified resource from storage.
     * created_by : Ruby
	  * created_on : 27-12-2018
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function delete(Request $request)
    {
		$returnData = array();
		if ($request->isMethod('post')){
			if(!empty($request['data']['id'])){
				try { 
					$holdData = DB::table('order_hold_master')->where('order_hold_master.oh_id', $request['data']['id'])->delete();
					if($holdData){
						$returnData = array('success' => config('messages.message.holdDeleted'));
					}else{
						$returnData = array('error' => config('messages.message.holdNotDeleted'));					
					}
				}catch(\Illuminate\Database\QueryException $ex){ 
				   $returnData = array('error' => config('messages.message.foreignKeyDeleteError'));
				}
			}else{
				$returnData = array('error' => config('messages.message.noRecordFound'));
			}
		}
		return response()->json($returnData);
    }
